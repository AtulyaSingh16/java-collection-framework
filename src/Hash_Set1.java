//1.Storing Unique Tags

import java.util.HashSet;

public class Hash_Set1 {
    public static void main(String[] args) {
        HashSet<String>tags=new HashSet<>();
        tags.add("java");
        tags.add("C");
        tags.add("C++");
        tags.add("Python");
        System.out.println("Tags : "+tags);

    }
}
